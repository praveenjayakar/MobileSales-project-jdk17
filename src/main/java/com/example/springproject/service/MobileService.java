    public List<String> getAllBrands() {
        return mobiles.stream().map(Mobile::getBrand).distinct().collect(Collectors.toList());
    }

    public List<Mobile> getMobilesByBrand(String brand) {
        return mobiles.stream().filter(m -> m.getBrand().equalsIgnoreCase(brand)).collect(Collectors.toList());
    }

    public Mobile getMobileById(int id) {
        return mobiles.stream().filter(m -> m.getId() == id).findFirst().orElse(null);
    }
